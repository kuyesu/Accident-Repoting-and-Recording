    private void searchTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTextFieldKeyReleased
        String firstName = searchTextField.getText();
        DefaultTableModel dtm = (DefaultTableModel) data_table.getModel();
            //showing and searching according to text given
            int r =data_table.getRowCount();
            
            while(r-->0){
                dtm.removeRow(r);
            }
            
            try {
                rs = SearchDatabaseRecords.pstmt(firstName);
                int col = rs.getMetaData().getColumnCount();
                while(rs.next()){
                    //using vector to insert row
                    java.util.Vector v = new java.util.Vector();
                    v.add(rs.getInt("incidence_id")+"/"+rs.getString("damaged"));
                    v.add(rs.getString("firstName"));
                    v.add(rs.getString("lastName"));
                    v.add(rs.getString("tel"));
                    v.add(rs.getString("email"));
                    v.add(rs.getString("region"));
                    v.add(rs.getString("district"));
                    v.add(rs.getString("place"));
                    v.add(rs.getString("category"));
                    v.add(rs.getString("emergency"));
                    v.add(rs.getString("injury"));
                    v.add(rs.getString("damaged"));
                    
                    dtm.addRow(v);
                    
                   
                }
        } catch (SQLException ex) {
            Logger.getLogger(Police_Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_searchTextFieldKeyReleased

    
